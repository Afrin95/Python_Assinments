"""Syntax:
while expression:
    statement(s)"""


# while loop
count = 0
while (count < 3):
    count = count + 1
    print("Hello Geek")


#  While Loop with else statement:
count = 0
while (count < 3):
    count = count + 1
    print("Hello Geek")
else:
    print("In Else Block")

#Infinite While Loop in Python
count = 0
while (count == 0):
    print("Hello Geek")